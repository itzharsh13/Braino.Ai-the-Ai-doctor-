# Helper function to get all resources
def get_all_resources():
    """Returns all mental health resources as a flat list"""
    all_resources = []
    for category, problems in MENTAL_HEALTH_RESOURCES.items():
        for problem in problems:
            problem_with_category = problem.copy()
            problem_with_category['category'] = category
            all_resources.append(problem_with_category)
    return all_resources

# Helper function to get resources by category
def get_resources_by_category(category):
    """Returns resources for a specific category"""
    return MENTAL_HEALTH_RESOURCES.get(category, [])

# Helper function to search resources
def search_resources(query):
    """Search resources by problem name or description"""
    query = query.lower()
    results = []
    for category, problems in MENTAL_HEALTH_RESOURCES.items():
        for problem in problems:
            if (query in problem['problem'].lower() or 
                query in problem['description'].lower() or
                any(query in symptom.lower() for symptom in problem['symptoms'])):
                problem_with_category = problem.copy()
                problem_with_category['category'] = category
                results.append(problem_with_category)
    return results
